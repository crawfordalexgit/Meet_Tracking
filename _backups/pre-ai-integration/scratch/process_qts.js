const result = {};

for (const gender in rawData) {
  result[gender] = {};
  for (const event in rawData[gender]) {
    result[gender][event] = { ages: {} };
    const eventData = rawData[gender][event];
    
    ages.forEach((age, idx) => {
      const autoIdx = idx * 2;
      const consIdx = idx * 2 + 1;
      
      const scTime = eventData.SC[autoIdx];
      const lcTime = eventData.LC[autoIdx];
      
      const scPts = calculateWAPoints(scTime, event, gender, 'SCM');
      const lcPts = calculateWAPoints(lcTime, event, gender, 'LCM');
      
      // We'll use the average or the higher one as the "Auto WA" benchmark
      const autoWA = Math.max(scPts, lcPts);
      
      result[gender][event].ages[age] = {
        SC: { time: scTime, pts: scPts },
        LC: { time: lcTime, pts: lcPts },
        autoWA
      };
    });
  }
}

